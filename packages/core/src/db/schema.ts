// Execution record for one AI turn. The conversation transcript ('turns', a later
// phase) is separate; this table is the durable queue: ordering, status, attempts,
// breaker linkage, cancellation, and visibility all live here.
export const messageQueue = pgTable(
  'message_queue',
  {
    id: uuid('id').primaryKey().defaultRandom(),
    // Ordering / single-flight domain. End-user turns: the conversation id.
    // Internal turns: a caller-derived lane uuid. No FK — conversations are a later phase.
    laneId: uuid('lane_id').notNull(),
    orgId: uuid('org_id').notNull().references(() => organizations.id, { onDelete: 'cascade' }),
    source: turnSource('source').notNull(),
    seq: bigint('seq', { mode: 'number' }).notNull(), // monotonic per lane
    status: queueStatus('status').notNull().default('queued'),
    payload: jsonb('payload').$type<unknown>().notNull(),
    attempts: integer('attempts').notNull().default(0),
    // Breaker-open deferrals (no provider call). Bounded separately from `attempts` so a long
    // breaker-open window fails the turn cleanly before Pub/Sub dead-letters it.
    deferrals: integer('deferrals').notNull().default(0),
    breakerKey: text('breaker_key'),
    cancelRequested: boolean('cancel_requested').notNull().default(false),
    error: jsonb('error').$type<unknown>(),
    createdAt: timestamp('created_at', { withTimezone: true }).notNull().defaultNow(),
    startedAt: timestamp('started_at', { withTimezone: true }),
    finishedAt: timestamp('finished_at', { withTimezone: true }),
  },
  (t) => [
    uniqueIndex('message_queue_lane_seq').on(t.laneId, t.seq),
    index('message_queue_lane_status').on(t.laneId, t.status),
    index('message_queue_org').on(t.orgId),
  ],
);

