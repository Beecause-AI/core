export const memberRole = pgEnum('member_role', ['owner', 'manager', 'user']);

export const organizations = pgTable('organizations', {
  id: uuid('id').primaryKey().defaultRandom(),
  name: text('name').notNull(),
  slug: text('slug').notNull().unique(),
  plan: text('plan').notNull().default('free'),
  // Realm lifecycle: 'pending' = signup recorded, realm not yet provisioned;
  // 'active' = realm + owner exist. Pending rows reserve the slug.
  status: text('status').notNull().default('active'),
  // Secret of the per-realm 'app' OIDC client (generated by us at provisioning).
  oidcClientSecret: text('oidc_client_secret'),
  // Identity Platform tenant for this org (replaces the Keycloak realm). Null
  // until provisioned; set at signup completion.
  idpTenantId: text('idp_tenant_id'),
  // Per-org federated SSO. ssoProvider is the Identity Platform provider id
  // (e.g. 'saml.acme-okta' / 'oidc.acme-entra'); null = password-only.
  ssoProvider: text('sso_provider'),
  ssoEnabled: boolean('sso_enabled').notNull().default(false),
  // Email that founded a pending org — lets the same signer re-request the verify
  // email; cleared on activation.
  pendingEmail: text('pending_email'),
  // Beta orgs get access to everything for free (bypasses plan limits once
  // enforcement exists; only stored/toggled for now — see super-ui spec).
  betaTester: boolean('beta_tester').notNull().default(false),
  // Knowledge Graph feature flag — org-scoped, off by default, toggled only in
  // the super console. Gates the project KG tab + its HTTP routes.
  kgEnabled: boolean('kg_enabled').notNull().default(false),
  // Hindsight agent flag — org-admin opt-in. When on: post-turn summaries are
  // produced and the system Hindsight agent is offerable for delegation.
  hindsightEnabled: boolean('hindsight_enabled').notNull().default(false),
  // Cost visibility flag — org-admin opt-in. When on: incident cost USD is shown
  // to org members (e.g. in the incident forecast banner).
  showCostUsd: boolean('show_cost_usd').notNull().default(false),
  // Debug flag — super-console toggle. When on: members see the fully-assembled
  // system prompt (persona + RCA preamble + integration skills) in the assistant
  // editor and team-proposal review, via the preview-prompt endpoint.
  debugEnabled: boolean('debug_enabled').notNull().default(false),
  approvalPolicy: jsonb('approval_policy').$type<{ writeToolsRequireApproval: boolean; overrides?: Record<string, boolean> } | null>(),
  createdAt: timestamp('created_at', { withTimezone: true }).notNull().defaultNow(),
});

